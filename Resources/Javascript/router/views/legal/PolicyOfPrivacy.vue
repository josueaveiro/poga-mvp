    <p>Puede gestionar las cookies modificando los parámetros de su navegador; también puede aceptar o rechazar la instalación de las mismas (cada navegador tiene su propia configuración).</p>

    <p>Uso de Web Beacons (también conocidos como etiquetas de Internet, etiquetas de píxel y clearGIFs).</p>

    <p>El Web Beacon es una imagen electrónica, llamada de un solo píxel (1x1) o GIF que puede reconocer información que es procesada en tu computadora, como el caso de las cookies, la hora y fecha en que el sitio y sus secciones son visualizados, para ofrecerle una mejor experiencia cuando utilice nuestras plataformas digitales</p>

